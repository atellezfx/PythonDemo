nombre = "Alejandro"
print(nombre)

# Concatenación
bienvenida = "Hola " + nombre + " ¿cómo estas?"
print(bienvenida)

# Interpolación con fstrings desde python 3.6
bienvenida = f"Hola {nombre} ¿cómo estas?"
print(bienvenida)

# Operadores de pertenencia

print( "ola" in bienvenida ) #true
print( "ola" not in bienvenida ) #false

del bienvenida # "del" operador para borrar datos

