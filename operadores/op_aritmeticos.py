# suma y resta ( + y - )
suma= 12 + 5
resta = 12 - 5

# multiplicación y división ( * y / )
multi = 12 * 5
division = 12 / 6 # devuelve un dato float

# potencia (exponente) (**)
exponente = 12 ** 5

# división baja (//)
division_baja = 12 // 5 # devuelve entero redondeado hacia abajo

# resto o módulo
modulo = 12 % 5

# Determinar el tipo de dato
tipo_de_dato = type(division)

print(f"Suma: {suma}")
print(f"Resta: {resta}")
print(f"Multiplicación: {multi}")
print(f"División: {division}")
print(f"Tipo: {tipo_de_dato}")
print(f"Exponente: {exponente}")
print(f"División baja: {division_baja}")
print(f"Módulo: {modulo}")
